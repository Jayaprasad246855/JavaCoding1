package com.app;

public class Employee{
	
	private int empId ;
	private long empSalary;
	private String empName;
	private String empDignation;

	
	
	// getters & setters
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public long getEmpSalary() {
		return empSalary;
	}
	public void setEmpSalary(long empSalary) {
		this.empSalary = empSalary;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpDignation() {
		return empDignation;
	}
	public void setEmpDignation(String empDignation) {
		this.empDignation = empDignation;
	}
	

}
